import { enUS, ja } from 'date-fns/locale';
import type { Locale as DateFnsLocale } from 'date-fns';

export type AppLocale = 'en' | 'ja';

export function resolveLocale(input?: string | null): AppLocale {
  if (!input) return 'en';
  const normalized = input.toLowerCase();
  if (normalized.startsWith('ja')) return 'ja';
  return 'en';
}

export function getDatePickerLocale(locale: AppLocale): DateFnsLocale {
  return locale === 'ja' ? ja : enUS;
}

export function formatDateTime(value: string | Date, locale: AppLocale, options?: Intl.DateTimeFormatOptions) {
  return new Date(value).toLocaleString(locale, options ?? { dateStyle: 'medium', timeStyle: 'short' });
}

export function formatTime(value: string | Date, locale: AppLocale, options?: Intl.DateTimeFormatOptions) {
  return new Date(value).toLocaleTimeString(locale, options ?? { hour: '2-digit', minute: '2-digit' });
}

